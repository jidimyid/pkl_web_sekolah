        <h2><strong>Sejarah</strong></h2>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;"><strong>1938</strong> : Tahun ini adalah tahun dimana untuk pertama kalinya telah berdiri sekolah percetakan di Indonesia oleh yayasan Karmel dan bertempat di Probolingo pada rumah anak yatim bernama Santo Clemens. Sekolah percetakan ini dipimpin oleh Frater L.C Lommelaars hingga jepang datang, beberapa guru ditawan dan mesin-mesin dirampas, sehingga sekolah ini ditutup.</p><br>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;">Setelah Pendudukan Jepang berakhir, maka pihak yayasan Karmel berusaha mencari alat-alat, mesin-mesin dan lain sebagainya yang tempatnya tersebar di seluruh Jawa. Kemudian Sejarah resmi sekolah ini pindah ketangan Pemerintah dibawah I.P.P.I Jakarta. Karena sempitnya ruangan maka diusahakan gedung baru milik yayasan Katholik St. Josef yang bersifat sementara di jalan Semeru 36 Malang dan masih di bawah pimpinan H.C Lommelaars dan Fr. M. Vincentius van der Waarden.</p><br>
        <img class="col-md-4" src="https://smkn4malang.sch.id/wp-content/uploads/2020/03/TD-3.jpg" alt="smkn4malang" style="float: center; width: 800px; margin-left: 100px;">
        <p class="mt-5" style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;"><strong>1953</strong> : Akhir tahun 1953 dibangun gedung baru di Jalan Bengawan Solo yang dipimpin oleh Frater M. Bernardo.</p><br>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;"><strong>1954</strong> : Pada November 1954 mulailah ditempati gedung baru di Jalan Bengawan Solo 1 Malang. Dalam tempo 1 minggu pekerjaan memindahkan mesin mesin beserta pemasangannya telah selesai berkat bantuan dari kepala montir dari NIMEF.</p><br>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;">Pada tahun 1954 Sekolah Kerajinan Negeri (SKN) Malang diganti menjadi Sekolah Teknik Menengah Bagian Percetakan STM Bagian Percetakan dengan masa studi selama 3 tahun. Oleh Keuskupan Malang sebagai Kepala Sekolah pada waktu itu ditunjuk Fr. Nolascus Waijers menggantikan Kepala Sekolah yang lama, bersama dengan itu lokasi sekolah juga pindah ke jalan Bengawan Solo No.38 yang sekarang diganti menjadi Jl. R. Tumenggung Soerjo No. 38 Malang.</p><br>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;"><strong>1995</strong> : Akhir bulan Oktober 1955 H.C Lommelaars berangkat ke Belanda dan sebagai pengganti diangkatnya Fr.M.Nolascus Wayers. Pada tahun ini sekolah ditutup kembali karena tidak dapat membayar mesin pemotong kertas dan mesin penggaris yang telah 4 tahun di pakai dan masih berstatus pinjaman. Pada akhir januari 1956 kedua mesin telah diambil kembali oleh pimilik, Lettergieterij Amsterdam bagian Surabaya. Sekolah terpaksa ditutup walaupun hal ini tak dikehendaki. Tetapi sesudah seminggu datanglah telegram dari Jakarta bahwa sekolah harus dibuka kembali. Dan beberapa hari kemudian datanglah kedua mesin tersebut dari Surabaya yang telah dibayarkan.</p><br>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;"><strong>1956</strong> : Pembukaan Sekolah Percetakan Negeri Malang pada pagi hari, sabtu tanggal 7 April 1956, Hadir pada waktu itu Wali Kota Malang. M. Sardjono, Bupati sebagai wakil Residen Malang. R. Soeroso. Sang merah putih naik, disertai lagu Indonesia Raya dengan musik S.G.A.K. dibawah pimpinan Frater Andre Formanoy. Pengguntingan pita dilakukan oleh Walikota Sardjono. Setelah kata pembukaan dari Ketua Panitia (Prajitno) maka dilanjutkan oleh Kepala Sekolah, Feter M. Nolaszus Waijers yang mengatakan betapa pentingnya sekolah ini di Indonesia, terutama dalam Industrialisasi dan masa pembangunan seperti sekarang ini. Kegiatan ini berakhir hingga dini hari pukul 9.30 malam. <br>

Akhir tahun 1956 banyak mesin baru yang datang ke sekolah ini dari Colombo Plan yang diterima dengan cuma-cuma.</p><br>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;"><strong>1957</strong> : Pada tahun 1957 Sekolah Teknik Menengah Bagian Percetakan diganti lagi menjadi Sekolah Guru Pendidikan Teknik Percetakan (SGPT Percetakan). Tahun 1959 diganti menjadi Sekolah Menengah Teknik Grafika Malang (SMT Grafika) dan ditunjuk sebagai Kepala Sekolah waktu itu M.Sultany Arief.</p><br>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;"><strong>1996</strong> : Tahun 1996 SMT Grafika berubah nama menjadi Sekolah Menengah Kejuruan Negeri 4 Malang (SMKN 4 Malang),bersama dengan itu lokasi juga pindah dari Jl. R. Tumenggung Soerjo No. 38 Malang ke Jl Tanimbar No. 22 Malang.</p><br>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;">Tahun 2002 SMK Negeri 4 Malang membuka bidang keahlian baru yakni Teknologi Informasi (TI).</p><br>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;">Tahun 2006 SMK Negeri 4 Malang membuka bidang keahlian baru Animasi.</p><br>
        <p style="font-size: 20px; margin-top: 5px; margin-bottom: 5px;">Tahun 2008 Meraih ISO 9001 : 2000 Sistem Manajemen Mutu.</p><br><br>
